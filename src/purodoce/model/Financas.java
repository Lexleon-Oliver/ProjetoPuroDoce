
package purodoce.model;

/**
 *
 * @author lex
 */
public class Financas {
    private float receita;
    private float despesa;
    private float receber;
    private float saldo;

    public float getReceita() {
        return receita;
    }

    public void setReceita(float receita) {
        this.receita = receita;
    }

    public float getDespesa() {
        return despesa;
    }

    public void setDespesa(float despesa) {
        this.despesa = despesa;
    }

    public float getReceber() {
        return receber;
    }

    public void setReceber(float receber) {
        this.receber = receber;
    }

    public float getSaldo() {
        return saldo;
    }

    public void setSaldo(float saldo) {
        this.saldo = saldo;
    }
    
    
    
}






