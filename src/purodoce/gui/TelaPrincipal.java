    private void BotaoSalvarProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoSalvarProdActionPerformed
        switch (botaoFuncao) {
            case "cadastrar":
                if (validarProduto()){
                    salvarProduto();
                    preencherProdutos();
                }else{
                    JOptionPane.showMessageDialog(null, "Corrija os problemas relatados e tente novamente","Atenção",JOptionPane.INFORMATION_MESSAGE);
                }

            break;
            case "remover":
                if(CampoIdProd.getText().equals("")){
                    JOptionPane.showMessageDialog(null, "Selecione um produto para remover","Produto Não Selecionado",JOptionPane.ERROR_MESSAGE);
                }else{
                    removerProduto(Long.parseLong(CampoIdProd.getText()));
                    preencherProdutos();
                }
            break;
            default:
                if(CampoIdProd.getText().equals("")){
                    JOptionPane.showMessageDialog(null, "Selecione um produto para incrementar","Produto Não Selecionado",JOptionPane.ERROR_MESSAGE);
                }else{
                    if (validarProduto()){
                        editarProduto();
                        preencherProdutos();
                    }else{
                        JOptionPane.showMessageDialog(null, "Corrija os problemas relatados e tente novamente","Atenção",JOptionPane.INFORMATION_MESSAGE);
                    }
                }        
        }
        limparProduto();
        habilitarProduto(false);

    }//GEN-LAST:event_BotaoSalvarProdActionPerformed

